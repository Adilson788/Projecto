valor1 = float(input('Primeiro Valor: '))
valor2 = float(input('Segundo Valor'))
if valor1 > valor2:
    print(f'O primeiro Valor é Maior, nesse caso {valor1}')
elif valor1 < valor2:
    print(f'O Segundo valor é maior, neste caso é {valor2}')
else:
    print(f'Nenhum valor e maior do que o outro, os dois são iguais')
